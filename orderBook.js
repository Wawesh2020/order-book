function reconcileOrder(book){
  let order = 0
  let existing = []

  for(let i = 0; i < book.length;i++){
    if(book[i] < 0){
      order = book[i].push()
    }
    return existing
  }
  if(book[i] === existing){
    order = book[i].push()
  }

  if(book[i] != existing){
    order = book[i].push()
  }

    
}



module.exports = reconcileOrder